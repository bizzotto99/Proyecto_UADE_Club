package com.ecommerce.camisetas.model.enums;

// Clasifica las camisetas según su tipo/uso dentro de un equipo.
// Permite filtrar y organizar el catálogo por tipo de camiseta.
public enum TipoProducto {

    // Camiseta del equipo local (color principal del club).
    TITULAR,

    // Camiseta alternativa usada cuando los colores chocan con el rival.
    SUPLENTE,

    // Tercera equipación, generalmente con un diseño más especial o limitado.
    TERCERA
}
